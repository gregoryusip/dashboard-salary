          desimal = ang.substr(idxPecahan + 1, ang.length),
          rupiah = split[0].substr(0, sisa),
          separator = '',
          ribuan = split[0].substr(sisa).match(/\d{3}/gi)
        if (ribuan) {
          separator = sisa ? '.' : ''
          rupiah += separator + ribuan.join('.')
        }
        if (desimal) {
          rupiah = rupiah + ',' + desimal
